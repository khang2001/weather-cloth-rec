// Utility function to get icon by name
export const getLocationIcon = (iconName) => {
  const icons = {
    home: HomeIcon,
    work: WorkIcon,
    beach: BeachIcon,
    city: CityIcon,
    mountain: MountainIcon,
    airport: AirportIcon,
    pin: PinIcon,
    star: StarIcon,
    map: MapIcon,
  };
  
  return icons[iconName?.toLowerCase()] || PinIcon;
};

